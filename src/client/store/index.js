/**
 * Sort array of objects based on another array
 */

function mapOrder (array, order, key) {
  const sorter = function (a, b) {
    const A = a[key]
    const B = b[key]

    if (order.indexOf(A) > order.indexOf(B)) {
      return 1
    } else {
      return -1
    }
  }

  array.sort(sorter)
  return array
}

